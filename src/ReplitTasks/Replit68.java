package ReplitTasks;

public class Replit68 {
    public static void main(String[] args) {

        for (int i = 1; i < 4; i++) {
            if (i == 1) {
                for (int j = 1; j <= 4; j++) {
                    System.out.print("$");
                }
            } else if (i == 2) {
                for (int j = 1; j < 2; j += 2) {
                    System.out.print("$");
                }
            } else if (1 == 3) {
                for (int j = 1; j < 2; j += 2) {
                    System.out.print("$");
                }
            } else if (i == 4) {
                for (int j = 0; j <= 4; j++) {
                    System.out.print("$");
                }
            }
            System.out.println();
        }
    }
}
